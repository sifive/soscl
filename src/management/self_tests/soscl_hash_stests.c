//SHA test vectors from NIST
//https://csrc.nist.gov/CSRC/media/Projects/Cryptographic-Standards-and-Guidelines/documents/examples/SHA512.pdf

int soscl_hash_sha256_stest(void)
{
  uint8_t hashref_sha256[SOSCL_SHA256_BYTE_HASHSIZE] = {0xba,0x78,0x16,0xbf,0x8f,0x01,0xcf,0xea,0x41,0x41,0x40,0xde,0x5d,0xae,0x22,0x23,0xb0,0x03,0x61,0xa3,0x96,0x17,0x7a,0x9c,0xb4,0x10,0xff,0x61,0xf2,0x00,0x15,0xad};
  uint8_t hash[SOSCL_SHA256_BYTE_HASHSIZE];
  uint8_t tab[3]={'a','b','c'};
  if(SOSCL_OK!=soscl_sha256(hash,tab,3))
    return(SOSCL_ERROR);
  if (soscl_memcmp(hash,hashref_sha256,SOSCL_SHA256_BYTE_HASHSIZE)!=0)
    return(SOSCL_ERROR);
  return(SOSCL_OK);
}

